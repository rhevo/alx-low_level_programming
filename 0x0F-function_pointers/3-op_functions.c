#include "3-calc.h"
#include <stdlib.h>
#include <stdio.h>

/**
 * op_add - addition
 * @a: first
 * @b: second
 *
 * Return: ret
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtraction
 * @a: ...
 * @b: ...
 *
 * Return: ret
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication
 * @a: ...
 * @b: ...
 *
 * Return: ret
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division
 * @a: ...
 * @b: ...
 *
 * Return: ret
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo
 * @a: ...
 * @b: ...
 *
 * Return: ret
 */

int op_mod(int a, int b)
{
	return (a % b);
}
